public class intarr {
    public static void main(String[] args) {

        int [] intarr = {10,20,30,50,60};

        for (int i : intarr){
            System.out.println(i+ " ");
        }

        for (int i = 0; i <intarr.length ; i++) {
            System.out.println(intarr[i] + " ");
        }
    }
}
